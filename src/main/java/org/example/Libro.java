package org.example;

public class Libro {
    private int id;
    private String nombre;
    private String autor;
    private String anio_publicion;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getAnio_publicion() {
        return anio_publicion;
    }

    public void setAnio_publicion(String anio_publicion) {
        this.anio_publicion = anio_publicion;
    }


}
